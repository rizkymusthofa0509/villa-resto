<script type="text/javascript">
 function proses_login() {
  var username = $('#username').val();
  var password = $('#password').val();
    // alert(username+" "+password);
    $.ajax({
      url:'<?php echo base_url() ?>portal/login/login',
      type:'POST',
      async:false,
      data:{
        username:username,
        password:password, 
      },
      success:function(data){
          if (data==1){
            window.location="<?php echo base_url() ?>app/";
          }else if (data==2){
            document.getElementById("status").innerHTML = "Bisnis Unit sudah tidak aktif. "; 
            $('#username').val("");
            $('#password').val("");
          }else{
            document.getElementById("status").innerHTML = "Username Atau Password salah!"; 
            $('#username').val("");
            $('#password').val("");
          }
      }
    });
  }
</script>